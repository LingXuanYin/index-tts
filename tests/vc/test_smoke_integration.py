class TestKMeansSmoke:
    """Verify mini k-means codebook on smoke content features."""

    @skip_no_smoke
    def test_kmeans_codebook_exists(self):
        ckpt = os.path.join(SMOKE_DATA_DIR, "kmeans_codebook.pt")
        assert os.path.exists(ckpt), "K-means codebook missing from data/vc/smoke/"

    @skip_no_smoke
    def test_kmeans_quantize_to_vector_shape(self):
        """quantize_to_vector must return (B, T, 256) from smoke content."""
        ckpt = os.path.join(SMOKE_DATA_DIR, "kmeans_codebook.pt")
        if not os.path.exists(ckpt):
            pytest.skip("K-means codebook not present")
        from indextts.vc.kmeans_quantizer import KMeansQuantizer
        quant = KMeansQuantizer(n_clusters=5, dim=256)
        quant.load(ckpt)

        # Load first content feature
        with open(SMOKE_MANIFEST, encoding="utf-8") as f:
            entries = [json.loads(l) for l in f if l.strip()]
        preproc_dir = os.path.abspath(SMOKE_PREPROC)
        stem = os.path.splitext(os.path.basename(entries[0]["audio_path"]))[0]
        content = np.load(os.path.join(preproc_dir, stem + ".content.npy"))
        batch = torch.from_numpy(content).unsqueeze(0).float()  # (1, T, 256)

        vec = quant.quantize_to_vector(batch)
        assert vec.shape == (1, content.shape[0], 256), f"Unexpected shape: {vec.shape}"


