class TestPhaseSwitchSmokeIntegration:
    """8.2 - Phase 1 freeze → Phase 2 unfreeze with real model."""

    @skip_full_smoke
    def test_phase1_freeze(self):
        from indextts.vc_train.model_adapter import build_vc_model
        from indextts.vc_train.train import set_phase1_frozen

        config = _load_config()
        model, reset_params, _ = build_vc_model(config)
        device = "cuda" if torch.cuda.is_available() else "cpu"
        model = model.to(device)

        reset_flat = [p for pg in reset_params for p in pg["params"]]
        reset_ids = {id(p) for p in reset_flat}
        set_phase1_frozen(model, reset_flat)

        # Reset params: trainable
        assert all(p.requires_grad for p in reset_flat), "Reset params must be trainable"
        # Finetune params: frozen
        finetune = [p for p in model.parameters() if id(p) not in reset_ids]
        assert all(not p.requires_grad for p in finetune), "Finetune params must be frozen"

    @skip_full_smoke
    def test_phase2_unfreeze_rebuilds_optimizer(self):
        from indextts.vc_train.model_adapter import build_vc_model
        from indextts.vc_train.train import (
            set_phase1_frozen, set_phase2_unfrozen, build_optimizer
        )

        config = _load_config()
        model, reset_params, finetune_params = build_vc_model(config)
        device = "cuda" if torch.cuda.is_available() else "cpu"
        model = model.to(device)

        reset_flat = [p for pg in reset_params for p in pg["params"]]
        set_phase1_frozen(model, reset_flat)
        set_phase2_unfrozen(model)

        # All params trainable
        assert all(p.requires_grad for p in model.parameters())

        # Rebuild Phase 2 optimizer
        opt = build_optimizer(reset_params, finetune_params, phase=2, reset_lr=1e-4, finetune_lr=1e-5)
        assert len(opt.param_groups) == 2
        lrs = [g["lr"] for g in opt.param_groups]
        assert 1e-4 in lrs and 1e-5 in lrs


