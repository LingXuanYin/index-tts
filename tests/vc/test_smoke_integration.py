"""
tests/vc/test_smoke_integration.py

Group 7-8 smoke integration tests (tasks 7.3-8.5).

These tests use the pre-generated mock smoke data in data/vc/smoke/ to verify
the full training pipeline: dataloader → model forward → loss → backward →
optimizer step → checkpoint save/load → tensorboard.

Prerequisites (generated by tools/generate_smoke_data.py or the generate script):
  - data/vc/smoke/manifest.jsonl (5 entries, 2 speakers)
  - data/vc/smoke/preprocessed/*.content.npy / .f0.npy / .mel.npy / .style.npy
  - data/vc/smoke/kmeans_codebook.pt
  - checkpoints/s2mel.pth (real TTS weights)

Loss threshold calibrated from first run (2026-04-07):
  - Phase 1, step 1: loss ≈ 1.48 (flow matching CFM loss)
  - Conservative threshold = 500.0 (allows for random init variance)

Tasks covered:
  8.1 - single-step forward/backward/optimizer
  8.2 - Phase 1 → Phase 2 switch (requires_grad + optimizer rebuild)
  8.3 - checkpoint save/load roundtrip
  8.4 - eval smoke (covered by tests/vc/test_eval.py unit tests)
  8.5 - tensorboard event file generation
"""
