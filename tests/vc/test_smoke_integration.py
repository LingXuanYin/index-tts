class TestTensorboardSmoke:
    """8.5 - Verify tensorboard event file is generated."""

    def test_tensorboard_writer_creates_event_file(self, tmp_path):
        from torch.utils.tensorboard import SummaryWriter

        tb_dir = str(tmp_path / "tb_smoke")
        writer = SummaryWriter(log_dir=tb_dir)
        writer.add_scalar("train/loss", 1.48, global_step=1)
        writer.add_scalar("train/lr_group0", 1e-4, global_step=1)
        writer.flush()
        writer.close()

        event_files = [
            f for f in os.listdir(tb_dir)
            if f.startswith("events.out.tfevents")
        ]
        assert len(event_files) > 0, "No tensorboard event files found!"
        event_size = os.path.getsize(os.path.join(tb_dir, event_files[0]))
        assert event_size > 0, "Tensorboard event file is empty!"
