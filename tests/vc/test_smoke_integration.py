class TestSmokeDataIntegrity:
    """Verify smoke manifest and preprocessed feature files are valid."""

    @skip_no_smoke
    def test_manifest_has_five_entries(self):
        with open(SMOKE_MANIFEST, encoding="utf-8") as f:
            entries = [json.loads(l) for l in f if l.strip()]
        assert len(entries) == 5, f"Expected 5 entries, got {len(entries)}"

    @skip_no_smoke
    def test_manifest_has_two_speakers(self):
        with open(SMOKE_MANIFEST, encoding="utf-8") as f:
            entries = [json.loads(l) for l in f if l.strip()]
        speakers = {e["speaker_id"] for e in entries}
        assert len(speakers) >= 2, f"Expected ≥2 speakers, got {speakers}"

    @skip_no_smoke
    def test_feature_files_exist(self):
        with open(SMOKE_MANIFEST, encoding="utf-8") as f:
            entries = [json.loads(l) for l in f if l.strip()]
        preproc_dir = os.path.abspath(SMOKE_PREPROC)
        for e in entries:
            stem = os.path.splitext(os.path.basename(e["audio_path"]))[0]
            base = os.path.join(preproc_dir, stem)
            for ext in [".content.npy", ".f0.npy", ".mel.npy", ".style.npy"]:
                assert os.path.exists(base + ext), f"Missing: {base + ext}"

    @skip_no_smoke
    def test_feature_shapes(self):
        with open(SMOKE_MANIFEST, encoding="utf-8") as f:
            entries = [json.loads(l) for l in f if l.strip()]
        preproc_dir = os.path.abspath(SMOKE_PREPROC)
        for e in entries:
            stem = os.path.splitext(os.path.basename(e["audio_path"]))[0]
            base = os.path.join(preproc_dir, stem)
            content = np.load(base + ".content.npy")
            f0 = np.load(base + ".f0.npy")
            mel = np.load(base + ".mel.npy")
            style = np.load(base + ".style.npy")
            # Shape checks
            assert content.shape[1] == 256, f"{stem}: content dim={content.shape[1]} != 256"
            assert mel.shape[0] == 80, f"{stem}: mel n_mels={mel.shape[0]} != 80"
            assert style.shape == (192,), f"{stem}: style shape={style.shape} != (192,)"
            assert f0.shape[0] == content.shape[0], (
                f"{stem}: f0 T={f0.shape[0]} != content T={content.shape[0]}"
            )
            # No NaN
            assert not np.any(np.isnan(content)), f"{stem}: NaN in content"
            assert not np.any(np.isnan(mel)), f"{stem}: NaN in mel"
            # voiced_ratio >= 0.1
            voiced_ratio = float(np.sum(f0 > 0)) / float(len(f0))
            assert voiced_ratio >= 0.1, f"{stem}: voiced_ratio={voiced_ratio:.3f} < 0.1"

    @skip_no_smoke
    def test_vcdata_loads_from_smoke(self):
        """VCDataset should load all 5 smoke entries without error."""
        entries = _load_smoke_entries()
        from indextts.vc_train.dataset import VCDataset
        ds = VCDataset(entries=entries, f0_strategy="source_contour")
        assert len(ds) == 5
        # Access first item
        item = ds[0]
        assert "content" in item
        assert "f0" in item
        assert "mel" in item
        assert "style" in item
        assert "prompt_mel" in item
        assert "prompt_content" in item


