class TestCheckpointSmokeIntegration:
    """8.3 - save checkpoint → rebuild model → load → 1 step → loss OK."""

    @skip_full_smoke
    def test_save_load_roundtrip(self, tmp_path):
        from indextts.vc_train.model_adapter import build_vc_model
        from indextts.vc_train.train import (
            vc_train_step, save_checkpoint, load_checkpoint, build_optimizer
        )

        config = _load_config()
        entries = _load_smoke_entries()
        batch = _build_smoke_batch(entries, batch_size=2)
        device = "cuda" if torch.cuda.is_available() else "cpu"

        # Model 1: run 1 step and save
        model1, reset_params, finetune_params = build_vc_model(config)
        model1 = model1.to(device)
        model1.train()
        opt1 = build_optimizer(reset_params, finetune_params, phase=1, reset_lr=1e-4, finetune_lr=1e-5)
        loss1 = vc_train_step(model1, batch, device=device)
        loss1.backward()
        opt1.step()

        ckpt_path = str(tmp_path / "smoke_ckpt.pth")
        metadata = {"current_phase": 1, "f0_strategy": "source_contour", "global_step": 1}
        save_checkpoint(model1, opt1, step=1, metadata=metadata, path=ckpt_path)
        assert os.path.exists(ckpt_path)

        # Model 2: load checkpoint, verify metadata, run 1 step
        model2, reset_params2, finetune_params2 = build_vc_model(config)
        model2 = model2.to(device)
        opt2 = build_optimizer(reset_params2, finetune_params2, phase=1, reset_lr=1e-4, finetune_lr=1e-5)

        loaded_meta = load_checkpoint(model2, opt2, ckpt_path)
        assert loaded_meta["current_phase"] == 1
        assert loaded_meta["f0_strategy"] == "source_contour"
        assert loaded_meta["global_step"] == 1

        model2.train()
        opt2.zero_grad()
        loss2 = vc_train_step(model2, batch, device=device)
        assert not torch.isnan(loss2), f"Post-load loss is NaN: {loss2}"
        assert loss2.item() > 0, f"Post-load loss must be positive: {loss2}"


